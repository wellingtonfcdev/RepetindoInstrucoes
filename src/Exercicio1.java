
/*
 * Contando de 1 a 10
Escreva um programa Java que imprime números de 1 à 10 utilizando 
a estrutura de repetição while. O programa deve imprimir cada número em uma linha diferente.
 */
public class Exercicio1 {
    public static void main(String[] args) {
        int numero = 1;

        while (numero <= 10) {
            System.err.println("Número :" + numero);
            numero++;
        }
    }
}
